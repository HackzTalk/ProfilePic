package com.hackztalk.profilepic.Models;

public class Home_Model {

int frame,btn;

    public Home_Model(int frame, int btn) {
        this.frame = frame;
        this.btn = btn;
    }

    public int getFrame() {
        return frame;
    }

    public void setFrame(int frame) {
        this.frame = frame;
    }

    public int getBtn() {
        return btn;
    }

    public void setBtn(int btn) {
        this.btn = btn;
    }
}
